package day03_EscapeChar_Variables;

public class Err_Vs_Out {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println("--------------------------");
        System.err.println("Hello World!");
    }

}

/*
        System.out.println() : used for regular outputs (Black- Siyah )
        System.err.println() : used for printing error messages ( Red Kirmizi)

 */
