package day15_If_Else__Switch_Loops_Summary;

public class C09_Nested_Loops {
    public static void main(String[] args) {

        for (int i = 0; i <3 ; i++) {
            System.out.print("A");
            for (int j = 0; j <5 ; j++) {
                System.out.print("B");
            }
            System.out.println();
        }




    }
}
